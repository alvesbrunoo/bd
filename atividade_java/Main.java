public class Main {
    public static void main(String[] args) {
        int a = 10;
        double b = 20.5;
        boolean c = true;

        double soma = a + b;
        System.out.println("Soma de a e b: " + soma);

        double multiplicacao = a * b;
        System.out.println("Multiplicação de a por b: " + multiplicacao);

        boolean cInverso = !c;
        System.out.println("Valor invertido de c: " + cInverso);
    }
}



